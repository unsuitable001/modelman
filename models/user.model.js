module.exports = (mongoose) => {
  const schema = mongoose.Schema({
    _id: String,
    name: String,
  });

  schema.method("toJSON", function () {
    const { __v, _id, ...object } = this.toObject();
    object.id = _id;
    return object;
  });

  const User = mongoose.model("user", schema);
  return User;
};
